        key = event.key()
        pause_keys = {self.Qt.Key.Key_Pause}
        break_key = getattr(self.Qt.Key, "Key_Break", None)
        if break_key is not None:
            pause_keys.add(break_key)

        if key == self.Qt.Key.Key_NumLock:
            self.cycle_profile_from_keyboard()
            return True

        if key in pause_keys:
            self.cycle_board_from_keyboard()
            return True

        return False

