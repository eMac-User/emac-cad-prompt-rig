def terminal_copy_result_log_line(result: Any) -> str:
    """Return a compact runtime log line for Copy Terminal Buffer provider results."""
    return (
        "COPY_TERMINAL_BUFFER result "
        f"ok={result.ok} "
        f"provider={result.provider} "
        f"quality={result.quality} "
        f"text_length={len(result.text)} "
        f"message={result.message}"
    )


class RuntimeModel:
    def __init__(self, spec: BoardSpec, clipboard: Clipboard) -> None:
        self.spec = spec
        self.clipboard = clipboard
        self.profile_index = 0
        self.board_index = 0
        self.status = "Ready"

    def copy_prompt_slot(self, slot: int) -> str:
        prompt = self.spec.prompt(self.profile_index, self.board_index, slot)
        body = self.spec.prompt_body(prompt)
        self.clipboard.copy(body)
        self.status = f"Copied: {slot} {prompt.label}"
        return body

    def paste_prompt_slot(self, slot: int) -> str:
        body = self.copy_prompt_slot(slot)
        self.status = f"Paste target unavailable; copied: {slot} {self.spec.prompt(self.profile_index, self.board_index, slot).label}"
        return body

    def copy_terminal_buffer(self, provider: Any = copy_terminal_buffer_with_provider) -> str:
        """Copy terminal text through the provider path used by the board button."""
        print("COPY_TERMINAL_BUFFER button_entry", flush=True)

        try:
            result = provider()
        except Exception as exc:  # noqa: BLE001 - button path must surface failures.
            self.status = f"ERROR: Copy Terminal Buffer provider failed: {exc}"
            print(
                "COPY_TERMINAL_BUFFER provider_exception "
                f"exception={type(exc).__name__} message={exc}",
                flush=True,
            )
            return ""

        print(terminal_copy_result_log_line(result), flush=True)

        if result.ok and result.text.strip():
            try:
                clipboard_backend = copy_terminal_text_to_system_clipboard(result.text, self.clipboard)
                print(
                    f"COPY_TERMINAL_BUFFER clipboard_written backend={clipboard_backend} text_length={len(result.text or '')}",
                    flush=True,
                )
            except Exception as exc:  # noqa: BLE001 - clipboard failures must be visible.
                self.status = f"ERROR: Copy Terminal Buffer clipboard write failed: {exc}"
                print(
                    "COPY_TERMINAL_BUFFER clipboard_exception "
                    f"provider={result.provider} quality={result.quality} "
                    f"exception={type(exc).__name__} message={exc}",
                    flush=True,
                )
                return ""

            self.status = result.message
            return result.text

        message = result.message
        if not message.startswith(("UNSUPPORTED:", "ERROR:")):
            message = (
                f"UNSUPPORTED: terminal provider returned {result.quality}; "
                f"clipboard was not changed. {result.message}"
            )

        self.status = message
        print(
            "COPY_TERMINAL_BUFFER not_copied "
            f"provider={result.provider} quality={result.quality} message={message}",
            flush=True,
        )
        return ""

    def cycle_profile(self) -> None:
        self.profile_index = (self.profile_index + 1) % max(1, len(self.spec.profiles))
        self.status = f"Profile: {self.spec.profiles[self.profile_index]['name']}"

    def set_profile(self, profile_index: int) -> None:
        if profile_index < 0 or profile_index >= len(self.spec.profiles):
            raise IndexError("profile_index out of range")
        self.profile_index = profile_index
        self.status = f"Profile: {self.spec.profiles[profile_index]['name']}"

    def cycle_board(self) -> None:
        self.board_index = (self.board_index + 1) % max(1, len(self.spec.boards))
        self.status = f"Board: {self.spec.boards[self.board_index]['name']}"

    def set_board(self, board_index: int) -> None:
        if board_index < 0 or board_index >= len(self.spec.boards):
            raise IndexError("board_index out of range")
        self.board_index = board_index
        self.status = f"Board: {self.spec.boards[board_index]['name']}"

    def active_title(self) -> str:
        profile = self.spec.profiles[self.profile_index]
        board = self.spec.boards[self.board_index]
        return f"{profile['name']} — {board['name']}"


