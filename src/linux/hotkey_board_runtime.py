def copy_terminal_text_to_system_clipboard(text: str, fallback_clipboard: Clipboard) -> str:
    """Copy Copy-Terminal-Buffer text to the real desktop clipboard.

    Native GUI toolkits can keep their own clipboard state under some Wayland/X11
    combinations. Copy Terminal Buffer is a system-facing action, so full
    transcript text is written through the external clipboard backend first.
    MemoryClipboard remains the test backend and still receives terminal text in
    unit tests.
    """
    if isinstance(fallback_clipboard, MemoryClipboard):
        fallback_clipboard.copy(text)
        return "memory"

    errors: list[str] = []
    try:
        external = ExternalClipboard()
        if external.copy_command:
            external.copy(text)
            return "external:" + " ".join(external.copy_command)
        errors.append("external clipboard command unavailable")
    except Exception as exc:  # noqa: BLE001 - fallback may still work.
        errors.append(f"external clipboard failed: {exc}")

    try:
        fallback_clipboard.copy(text)
        return type(fallback_clipboard).__name__
    except Exception as exc:  # noqa: BLE001 - report all attempted clipboard paths.
        errors.append(f"fallback clipboard failed: {exc}")
        raise RuntimeError("Could not write terminal buffer to clipboard. " + "; ".join(errors)) from exc


@dataclass(frozen=True)
class PromptRecord:
    section: str
    profile_id: str
    board_id: str
    slot: int
    label: str
    path: str


class BoardSpec:
    def __init__(self, repo_root: Path, spec_path: Path, data: dict[str, Any]) -> None:
        self.repo_root = repo_root
        self.spec_path = spec_path
        self.data = data
        self.product_name = data.get("product", {}).get("name", "Hotkey Board")
        self.folder_name = data.get("product", {}).get("folder_name", repo_root.name)
        model = data.get("model", {})
        self.profiles: list[dict[str, str]] = list(model.get("profiles", []))
        self.boards: list[dict[str, str]] = list(model.get("boards", []))
        self.prompts_per_board = int(model.get("prompts_per_board", 0) or 0)
        self.settings_rel = data.get("source", {}).get("settings_path", "config/settings.ini")
        self.prompts: list[PromptRecord] = [
            PromptRecord(
                section=item["section"],
                profile_id=item["profile_id"],
                board_id=item["board_id"],
                slot=int(item["slot"]),
                label=item["label"],
                path=item["path"],
            )
            for item in data.get("prompts", [])
        ]
        self._prompt_by_key = {(p.profile_id, p.board_id, p.slot): p for p in self.prompts}

    @classmethod
    def load(cls, repo_root: Path, spec_rel_or_abs: str) -> "BoardSpec":
        spec_path = Path(spec_rel_or_abs)
        if not spec_path.is_absolute():
            spec_path = repo_root / spec_path
        data = json.loads(spec_path.read_text(encoding="utf-8"))
        schema = data.get("schema_version")
        if schema != "linux-hotkey-board-spec-v1":
            raise ValueError(f"Unsupported board spec schema: {schema}")
        return cls(repo_root=repo_root, spec_path=spec_path, data=data)

    def prompt(self, profile_index: int, board_index: int, slot: int) -> PromptRecord:
        profile = self.profiles[profile_index]
        board = self.boards[board_index]
        key = (profile["id"], board["id"], slot)
        if key not in self._prompt_by_key:
            raise KeyError(f"No prompt for {profile['id']}.{board['id']}.{slot:02d}")
        return self._prompt_by_key[key]

    def prompt_body(self, prompt: PromptRecord) -> str:
        prompt_path = self.repo_root / prompt.path
        body = prompt_path.read_text(encoding="utf-8")
        if not body.strip():
            raise ValueError(f"Prompt file is empty: {prompt.path}")
        return body

    def validate_prompt_files(self) -> list[str]:
        errors: list[str] = []
        for prompt in self.prompts:
            path = self.repo_root / prompt.path
            if not path.is_file():
                errors.append(f"Missing prompt Markdown file: {prompt.path}")
                continue
            if not path.read_text(encoding="utf-8").strip():
                errors.append(f"Empty prompt Markdown file: {prompt.path}")
        return errors


class SettingsStore:
    def __init__(self, path: Path, initial: dict[str, str] | None = None) -> None:
        self.path = path
        self.values = dict(DEFAULT_SETTINGS)
        if initial:
            self.values.update({key: str(value) for key, value in initial.items() if key in DEFAULT_SETTINGS})
        self.load()

    def load(self) -> None:
        if not self.path.exists():
            return
        cp = configparser.ConfigParser()
        cp.read(self.path, encoding="utf-8")
        if not cp.has_section("settings"):
            return
        for key in DEFAULT_SETTINGS:
            value = cp["settings"].get(key, self.values[key]).strip().lower()
            self.values[key] = "1" if value in {"1", "true", "yes", "on"} else "0"

    def save(self) -> None:
        self.path.parent.mkdir(parents=True, exist_ok=True)
        cp = configparser.ConfigParser()
        cp["settings"] = self.values
        with self.path.open("w", encoding="utf-8") as handle:
            cp.write(handle)

    def enabled(self, key: str) -> bool:
        return self.values.get(key, "0") == "1"

    def set_enabled(self, key: str, enabled: bool, *, persist: bool = True) -> None:
        self.values[key] = "1" if enabled else "0"
        if persist:
            self.save()


