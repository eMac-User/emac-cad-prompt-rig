    def handle_path(self, handler: BaseHTTPRequestHandler) -> None:
        parsed = urlparse(handler.path)
        qs = parse_qs(parsed.query)
        try:
            if parsed.path == "/":
                self._html_response(handler, self.page())
                return
            if parsed.path == "/api/prompt":
                slot = int(qs.get("slot", ["1"])[0])
                prompt = self.spec.prompt(self.model.profile_index, self.model.board_index, slot)
                body = self.spec.prompt_body(prompt)
                self.model.status = f"Copied: {slot} {prompt.label}"
                self._json_response(handler, {"body": body, "status": self.model.status})
                return
            if parsed.path == "/api/switch-profile":
                self.model.set_profile((self.model.profile_index + 1) % len(self.spec.profiles))
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/set-profile":
                self.model.set_profile(int(qs.get("index", ["0"])[0]))
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/set-board":
                self.model.set_board(int(qs.get("index", ["0"])[0]))
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/toggle-numpad":
                enabled = qs.get("enabled", ["0"])[0] == "1"
                if self.settings.enabled("remember_numpad_mode"):
                    self.settings.set_enabled("enable_numpad_mode", enabled)
                else:
                    self.settings.values["enable_numpad_mode"] = "1" if enabled else "0"
                self.model.status = f"Numpad: {'On' if enabled else 'Off'}"
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/toggle-remember-numpad":
                enabled = qs.get("enabled", ["0"])[0] == "1"
                self.settings.set_enabled("remember_numpad_mode", enabled)
                self.model.status = f"Remember Numpad Mode: {'On' if enabled else 'Off'}"
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/open-prompts":
                prompt = self.spec.prompt(self.model.profile_index, self.model.board_index, 1)
                folder = self.spec.repo_root / prompt.path
                subprocess.Popen(["xdg-open", str(folder.parent)])
                self.model.status = f"Opened prompt folder: {folder.parent}"
                self._json_response(handler, {"status": self.model.status})
                return
            if parsed.path == "/api/shutdown":
                self._json_response(handler, {"status": "Board closed"})
                if self.server is not None:
                    threading.Thread(target=self.server.shutdown, daemon=True).start()
                return
            self._json_response(handler, {"error": "not found"}, status=404)
        except Exception as exc:  # noqa: BLE001
            self._json_response(handler, {"error": str(exc), "status": f"ERROR: {exc}"}, status=500)

    def run(self) -> None:
        app = self

        class Handler(BaseHTTPRequestHandler):
            def do_GET(self) -> None:  # noqa: N802 - stdlib callback name.
                app.handle_path(self)

            def log_message(self, _format: str, *args: Any) -> None:
                return

        self.server = ThreadingHTTPServer(("127.0.0.1", 0), Handler)
        self.server.app = self  # type: ignore[attr-defined]
        port = int(self.server.server_address[1])
        self.url = f"http://127.0.0.1:{port}/"
        print("## Linux Hotkey Board Web GUI")
        print(f"Product: {self.spec.product_name}")
        print(f"URL: {self.url}")
        print("Tkinter is unavailable, so the runtime opened a local browser GUI fallback.")
        print("Close the board from the browser Close button, or press Ctrl+C in this terminal.")
        webbrowser.open(self.url)
        try:
            self.server.serve_forever()
        except KeyboardInterrupt:
            pass
        finally:
            self.server.server_close()


