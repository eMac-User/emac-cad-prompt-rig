class Clipboard(Protocol):
    def copy(self, text: str) -> None: ...
    def paste(self) -> str: ...


class MemoryClipboard:
    def __init__(self) -> None:
        self.value = ""

    def copy(self, text: str) -> None:
        self.value = text

    def paste(self) -> str:
        return self.value


class ExternalClipboard:
    def __init__(self) -> None:
        self.copy_command = self._detect_copy_command()
        self.paste_command = self._detect_paste_command()

    @staticmethod
    def _detect_copy_command() -> list[str] | None:
        if shutil.which("wl-copy"):
            return ["wl-copy"]
        if shutil.which("xclip"):
            return ["xclip", "-selection", "clipboard"]
        if shutil.which("xsel"):
            return ["xsel", "--clipboard", "--input"]
        return None

    @staticmethod
    def _detect_paste_command() -> list[str] | None:
        if shutil.which("wl-paste"):
            return ["wl-paste", "--no-newline"]
        if shutil.which("xclip"):
            return ["xclip", "-selection", "clipboard", "-out"]
        if shutil.which("xsel"):
            return ["xsel", "--clipboard", "--output"]
        return None

    def copy(self, text: str) -> None:
        if not self.copy_command:
            raise RuntimeError("No clipboard command found. Install wl-clipboard, xclip, or xsel, or use Tk clipboard from the GUI.")
        subprocess.run(self.copy_command, input=text.encode("utf-8"), check=True)

    def paste(self) -> str:
        if not self.paste_command:
            return ""
        proc = subprocess.run(self.paste_command, check=False, capture_output=True)
        if proc.returncode != 0:
            return ""
        return proc.stdout.decode("utf-8", errors="replace")


class TkClipboard:
    def __init__(self, root: Any) -> None:
        self.root = root

    def copy(self, text: str) -> None:
        self.root.clipboard_clear()
        self.root.clipboard_append(text)
        self.root.update_idletasks()

    def paste(self) -> str:
        try:
            return str(self.root.clipboard_get())
        except Exception:  # noqa: BLE001 - Tk raises TclError without clipboard data.
            return ""

